/**
 * _strlen -  returns the length of a string.
 * @s: string to be checked
 *
 * Return: string length.
 */

int _strlen(char *s)
{
	int len;

	for (len = 0; s[len] != '\0'; ++len)
	{
		/**
		 * s[len] != '\0' loop continues as long as the
		 * current character is not the null terminator character
		*/
	}
	return (len);
}
